/**
 * Return a valid access token, refreshing automatically if expired.
 *
 * Throws if:
 *   - No tokens are stored (user needs to run OAuth flow)
 *   - Refresh token has expired (user needs to re-authenticate)
 *
 * Callers (tools.ts) catch these errors and return appropriate MCP error responses.
 */
export async function getValidAccessToken(
  clientId: string,
  clientSecret: string
): Promise<string> {
  const tokens = await loadTokensFromKeychain();

  if (!tokens) {
    throw new Error(
      "not_authenticated: No tokens found. Run the OAuth 2.0 PKCE flow first."
    );
  }

  const isExpired = new Date(tokens.expires_at) <= new Date(Date.now() + 60_000);

  if (!isExpired) {
    return tokens.access_token;
  }

  // Token expired — attempt refresh
  const refreshed = await refreshAccessToken(
    tokens.refresh_token,
    clientId,
    clientSecret
  );
  await saveTokensToKeychain(refreshed);
  return refreshed.access_token;
}
