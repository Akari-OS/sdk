// ---------------------------------------------------------------------------
// PKCE helpers
// ---------------------------------------------------------------------------

/**
 * Generate a PKCE code_verifier (43–128 chars, URL-safe Base64).
 * Spec: RFC 7636
 */
export function generateCodeVerifier(): string {
  return crypto.randomBytes(32).toString("base64url");
}

/**
 * Derive code_challenge from code_verifier using S256 method.
 */
export function generateCodeChallenge(codeVerifier: string): string {
  return crypto
    .createHash("sha256")
    .update(codeVerifier)
    .digest("base64url");
}

/**
 * Build PKCE params (code_verifier, code_challenge, state) for a new auth request.
 */
export function buildPKCEParams(): PKCEParams {
  const code_verifier = generateCodeVerifier();
  return {
    code_verifier,
    code_challenge: generateCodeChallenge(code_verifier),
    state: crypto.randomBytes(16).toString("hex"),
  };
}

// ---------------------------------------------------------------------------
// Authorization URL builder
// ---------------------------------------------------------------------------

/**
 * Build the X OAuth 2.0 authorization URL.
 * The caller is responsible for opening this URL in a browser.
 *
 * @param clientId     X Developer App Client ID (from env: X_CLIENT_ID)
 * @param redirectUri  OAuth redirect URI (from env: OAUTH_REDIRECT_URI)
 * @param pkce         PKCE params generated by buildPKCEParams()
 */
export function buildAuthorizationUrl(
  clientId: string,
  redirectUri: string,
  pkce: PKCEParams
): string {
  const params = new URLSearchParams({
    response_type: "code",
    client_id: clientId,
    redirect_uri: redirectUri,
    scope: OAUTH_SCOPES.join(" "),
    state: pkce.state,
    code_challenge: pkce.code_challenge,
    code_challenge_method: "S256",
  });
  return `${X_OAUTH_AUTHORIZE_URL}?${params.toString()}`;
}

