/** プラットフォーム ID → アイコンコンポーネント */
export function PlatformIcon({ platformId, size = 16, className }: { platformId: string; size?: number; className?: string }) {
  const badge = getBadge(platformId);
  const IconComponent = PLATFORM_ICON_MAP[platformId];

  return (
    <span className="relative inline-flex" style={{ width: size, height: size }}>
      {IconComponent ? <IconComponent size={size} className={className} /> : <span style={{ fontSize: size * 0.8 }}>{getFallbackEmoji(platformId)}</span>}
      {badge && (
        <span className="absolute -bottom-0.5 -right-0.5 text-[7px] font-bold bg-background border border-border rounded-sm px-0.5 leading-none">
          {badge}
        </span>
      )}
    </span>
  );
}

const PLATFORM_ICON_MAP: Record<string, (props: IconProps) => React.ReactNode> = {
  x: XIcon,
  x_long: XIcon,
  ig_feed: InstagramIcon,
  ig_story: InstagramIcon,
  ig_reel: InstagramIcon,
  tiktok: TikTokIcon,
  youtube: YouTubeIcon,
  youtube_short: YouTubeIcon,
  threads: ThreadsIcon,
  note: NoteIcon,
  blog: BlogIcon,
  facebook: FacebookIcon,
};

function getBadge(id: string): string | null {
  switch (id) {
    case "x_long": return "+";
    case "ig_story": return "S";
    case "ig_reel": return "R";
    case "youtube_short": return "S";
    default: return null;
  }
}

function getFallbackEmoji(id: string): string {
  const map: Record<string, string> = { x: "𝕏", threads: "🧵", note: "📝", blog: "✍️", facebook: "📘" };
  return map[id] ?? "📱";
}
